def fix_lesson3_manual_verify(file_path):
    with open(file_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
        
    count = 0
    for item in data:
        kana = item['data']['假名']
        current_pos = item['data']['词性']
        
        # 查找修正表
        if kana in CORRECT_POS_MAP:
            correct_pos = CORRECT_POS_MAP[kana]
            if current_pos != correct_pos:
                print(f"Fixing {kana}: {current_pos} -> {correct_pos}")
                item['data']['词性'] = correct_pos
                count += 1
        else:
            # 如果不在修正表中，保留原样，但打印警告
            # print(f"Warning: {kana} not in manual map. Keeping {current_pos}.")
            pass
        
    with open(file_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        
    print(f"Manually verified and fixed {count} items in {file_path}")

