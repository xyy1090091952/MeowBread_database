#    让我们暂时用 "形容动词" 代替 "二类形容词"，用 "形容词" 代替 "一类形容词"。
#    因为 "形容词" 在日语语法里专指 い形容词。
# 4. 多词性用 "、" 分隔。

def normalize_pos_strict(pos):
    # 替换规则
    pos = pos.replace("一类形容词", "形容词")
    pos = pos.replace("二类形容词", "形容动词")
    
    # 确保没有多余空格
    parts = pos.split("、")
    clean_parts = [p.strip() for p in parts]
    return "、".join(clean_parts)

def fix_lesson3_pos(file_path):
    with open(file_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
        
    for item in data:
        original_pos = item['data']['词性']
        new_pos = normalize_pos_strict(original_pos)
        item['data']['词性'] = new_pos
        
    with open(file_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        
    print(f"Processed {len(data)} items in {file_path}")

