def parse_line(line):
    kana_buf = ""
    kanji_res = ""
    kana_res = ""
    
    chars = list(line)
    for char in chars:
        if '\u4e00' <= char <= '\u9fff' or char == '々': # 汉字
            if kana_buf:
                kana_res += kana_buf
                kana_buf = ""
            kanji_res += char
        else: # 非汉字
            kana_buf += char
            
    if kana_buf:
        kana_res += kana_buf
        kanji_res += kana_buf
        
    has_kanji = False
    for char in kanji_res:
        if '\u4e00' <= char <= '\u9fff' or char == '々':
            has_kanji = True
            break
    if not has_kanji:
        kanji_res = ""
        
    return kana_res.strip(), kanji_res.strip()

def process_file(file_path):
    with open(file_path, 'r', encoding='utf-8') as f:
        lines = f.readlines()
        
    words = []
    i = 0
    while i < len(lines):
        line = lines[i].strip()
        if not line or line in ["本文", "文法", "聴解"]:
            i += 1
            continue
            
        if i + 2 < len(lines):
            raw_word = lines[i].strip()
            pos_raw = lines[i+1].strip()
            meaning = lines[i+2].strip()
            
            # 简单验证
            if not (("名" in pos_raw or "动" in pos_raw or "形" in pos_raw) and ("（" in pos_raw or pos_raw in ["名", "副", "形1", "形2"])):
                 # 容错：名・形2 这种
                 if not (pos_raw in ["名", "形1", "形2", "副"] or "・" in pos_raw):
                    i += 1
                    continue

            kana, kanji = parse_line(raw_word)
            
            # 使用手动映射表修正词性
            final_pos = "名词" # 默认
            if kana in CORRECT_POS_MAP:
                final_pos = CORRECT_POS_MAP[kana]
            else:
                pass
                
            words.append({
                "data": {
                    "假名": kana,
                    "汉字": kanji,
                    "中文": meaning,
                    "例句": "",
                    "词性": final_pos,
                    "日语干扰词": [],
                    "中文干扰词": []
                }
            })
            i += 3
        else:
            break
            
    return words

